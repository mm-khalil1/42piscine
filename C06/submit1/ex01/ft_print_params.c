#include <unistd.h>

int	main(int a, char **val)
{
	int	i;

	i = 1;
	while (i < a)
	{
		while (*val[i] != '\0')
		{
			write(1, val[i], 1);
			++val[i];
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
